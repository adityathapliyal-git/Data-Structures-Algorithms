public class Incre_Decre {
  public static void main(String[] args) {
    int x = 54;
    x++; 
    System.out.println(x); // 55
    x--; 
    System.out.println(x); // 54
    ++x; 
    System.out.println(x); // 55
    --x;
    System.out.println(x); // 54
  }
}
// Output : 55
//          54
//          55
//          54