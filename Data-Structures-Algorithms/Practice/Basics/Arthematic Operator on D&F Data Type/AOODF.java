public class AOODF {
  public static void main(String[] args) {
    float x=3,y=6;
    double a=5,b=2;
    // Float Operations
     System.out.println(x+y); // 6+3=9.0
     System.out.println(x-y); // 3-6=-3.0
     System.out.println(x*y); // 3x6=18.0
     System.out.println(x/y); // 3/6=0.5
    // Double Operations 
     System.out.println(a+b); // 5+2=7
     System.out.println(a-b); // 5-2=3.0
     System.out.println(a*b); // 5x2=10.0
     System.out.println(a/b); // 5/2=2.5
  }
}
/*Output : 9.0
          -3.0
          18.0
           0.5
           7.0
           3.0
          10.0
           2.5
 */